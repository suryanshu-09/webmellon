async function main() {
  // Assuming 'catalogues' is an object where the key is the catalogue name and value is an array of websites
  for (const [catalogueName, sites] of Object.entries(catalogues)) {
    // Upsert Catalogue (create or update if it already exists for the user)
    const catalogue = await prisma.catalogue.upsert({
      where: {
        user_catalogue_name_unique: {
          name: catalogueName,
          userId: user?.id ?? "",  // Ensure we're looking for the catalogue belonging to the current user
        },
      },
      update: {},
      create: {
        name: catalogueName,
        userId: user?.id ?? "",
      },
    })

    // Upsert Websites for this catalogue
    for (const site of sites) {
      await prisma.website.upsert({
        where: {
          user_website_name_unique: {
            name: site.name,
            userId: user?.id ?? "",  // Ensure we're looking for the website belonging to the current user
          },
        },
        update: {},
        create: {
          name: site.name,
          userId: user?.id ?? "",
          url: site.url,
          favicon: `https://www.google.com/s2/favicons?sz=64&domain=${new URL(site.url).hostname}`,
          catalogueId: catalogue.id,
        },
      })
    }
  }
  console.log('âœ… All websites seeded by category.')
}

